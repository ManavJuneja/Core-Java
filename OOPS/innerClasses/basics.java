package OOPS.innerClasses;

public class basics {
    /*

Type of inner class
1. Member Inner Class
A non-static inner class defined inside another class.
It has access to all members of the outer class (including private members).

2. Static Nested Class
A static class inside another class.
It cannot access non-static members of the outer class.
It behaves like a top-level class but is scoped inside another class.
It belongs to the outer class rather than instance of that class

3. Local Inner Class
Defined inside a method or block.
Can access local variables of the enclosing method only if they are final or effectively final.

4. Anonymous Inner Class
A class with no name, typically used for one-time use.
Created when a class needs to be subclassed or an interface needs to be implemented on the fly.


     */
}
